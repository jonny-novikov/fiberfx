#!/usr/bin/env python3
"""
verify_numbers.py — верификатор числовой части курса «Право повседневной жизни».

ВАЖНО про честность: право — НЕ математика. Конкретные нормы, сроки и ставки
МЕНЯЮТСЯ и зависят от ситуации и юрисдикции. Эта утилита НЕ проверяет «законность» —
она:
  (1) держит справочные правовые КОНСТАНТЫ в одном месте (single source of truth)
      с обязательной пометкой «сверять действующую редакцию на дату»;
  (2) проверяет АРИФМЕТИКУ калькуляторов курса (алименты, обязательная доля,
      сверхурочные, отпуск, сроки) — чтобы формула в JS совпадала с проверенной.

Перед версткой модуля с калькулятором: проверить формулу здесь, затем зеркалить в JS.
Каждую правовую цифру в модуле сопровождать дисклеймером и ссылкой на источник
(кодекс/закон + pravo.gov.ru / КонсультантПлюс / Гарант), с пометкой актуальной редакции.

Запуск:  python3 verify_numbers.py
"""
import math

ok = 0
def check(name, got, exp, tol=1e-9):
    global ok
    good = abs(got - exp) <= tol if isinstance(exp, (int, float)) else got == exp
    print(f"  {'✓' if good else '✗'} {name}: {got}")
    assert good, f"FAIL {name}: {got} != {exp}"
    ok += 1

# === Справочные правовые константы (СВЕРЯТЬ ДЕЙСТВУЮЩУЮ РЕДАКЦИЮ!) ===
# Значения приведены как ориентир для учебных калькуляторов. Перед публикацией
# модуля каждую цифру подтвердить по действующей редакции кодекса на pravo.gov.ru.
LEGAL = {
    'возврат_надлежащего_качества_дней': 14,      # ст. 25 ЗоЗПП (непродовольственный товар)
    'увольнение_по_собственному_предупреждение_дней': 14,  # ст. 80 ТК РФ (2 недели)
    'принятие_наследства_месяцев': 6,             # ст. 1154 ГК РФ
    'исковая_давность_общая_лет': 3,              # ст. 196 ГК РФ
    'ежегодный_отпуск_дней': 28,                  # ст. 115 ТК РФ (календарные дни)
    'испытательный_срок_макс_месяцев': 3,         # ст. 70 ТК РФ (общий случай)
    'алименты_доля': {1: 0.25, 2: 1/3, 3: 0.5},   # ст. 81 СК РФ (1/4, 1/3, 1/2 — 3 и более)
    'обязательная_доля_коэффициент': 0.5,         # ст. 1149 ГК РФ: не менее 1/2 законной доли
    'сверхурочные_первые_2ч_коэф': 1.5,           # ст. 152 ТК РФ
    'сверхурочные_далее_коэф': 2.0,               # ст. 152 ТК РФ
}

print("== Гл.2/3 · Сроки (календарные значения) ==")
check("Возврат товара надлежащего качества — 14 дней", LEGAL['возврат_надлежащего_качества_дней'], 14)
check("Предупреждение об увольнении — 14 дней", LEGAL['увольнение_по_собственному_предупреждение_дней'], 14)
check("Ежегодный отпуск — 28 дней", LEGAL['ежегодный_отпуск_дней'], 28)
# накопление отпуска ~2.33 дня в месяц
check("Накопление отпуска ≈ 2.33 дн/мес", round(28/12, 2), 2.33)

print("\n== Гл.4 · Алименты на детей (доля от дохода, ст. 81 СК РФ) ==")
def alimony(income, kids):
    share = LEGAL['алименты_доля'].get(min(kids, 3), 0.5) if kids >= 1 else 0
    return income * share
check("80000, 1 ребёнок → 20000 (1/4)", alimony(80000, 1), 20000.0)
check("90000, 2 детей → 30000 (1/3)", round(alimony(90000, 2), 2), 30000.0)
check("80000, 3 детей → 40000 (1/2)", alimony(80000, 3), 40000.0)
check("80000, 5 детей → 40000 (тоже 1/2, 3 и более)", alimony(80000, 5), 40000.0)

print("\n== Гл.5 · Обязательная доля (ст. 1149 ГК РФ: ≥ 1/2 законной доли) ==")
def mandatory_share(estate, heirs_by_law):
    """heirs_by_law — число наследников по закону той же очереди (равные доли)."""
    legal_share = estate / heirs_by_law
    return legal_share * LEGAL['обязательная_доля_коэффициент']
# Наследство 6 млн, по закону наследовали бы 2 ребёнка поровну → законная доля 3 млн,
# обязательная доля нетрудоспособного/несовершеннолетнего ребёнка = 1/2 от 3 млн = 1.5 млн
check("Наследство 6 млн, 2 наследника → обязательная доля 1.5 млн", mandatory_share(6_000_000, 2), 1_500_000.0)
check("  это 1/4 всего наследства", mandatory_share(6_000_000, 2) / 6_000_000, 0.25)

print("\n== Гл.3 · Сверхурочные (ст. 152 ТК РФ: первые 2ч ≥1.5×, далее ≥2×) ==")
def overtime_pay(hourly, hours):
    first = min(hours, 2) * hourly * LEGAL['сверхурочные_первые_2ч_коэф']
    rest = max(hours - 2, 0) * hourly * LEGAL['сверхурочные_далее_коэф']
    return first + rest
check("Ставка 300, 2 часа сверхурочных → 900", overtime_pay(300, 2), 900.0)
check("Ставка 300, 3 часа → 1500 (900 + 600)", overtime_pay(300, 3), 1500.0)
check("Ставка 300, 4 часа → 2100 (900 + 1200)", overtime_pay(300, 4), 2100.0)

print("\n== Гл.6 · Исковая давность и сроки ==")
check("Общий срок исковой давности — 3 года", LEGAL['исковая_давность_общая_лет'], 3)
check("Принятие наследства — 6 месяцев", LEGAL['принятие_наследства_месяцев'], 6)

print(f"\n✅ Арифметика калькуляторов проверена: {ok}. Формулы готовы к переносу в JS.")
print("⚠  Правовые КОНСТАНТЫ — справочные. Перед публикацией модуля сверить действующую")
print("   редакцию кодекса (ГК/ТК/СК РФ, ЗоЗПП, ГПК РФ) на pravo.gov.ru и в КонсультантПлюс/Гарант.")
