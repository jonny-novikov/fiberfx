#!/usr/bin/env python3
"""Проверка всех числовых примеров и формул курса ПЕРЕД вёрсткой страниц.
Запуск: python3 verify_physics.py  (все assert должны пройти)."""

def approx(a, b, tol=1e-6):
    return abs(a - b) <= tol * max(1.0, abs(b))

checks = []
def check(name, got, exp):
    ok = approx(got, exp)
    checks.append((name, got, exp, ok))
    return ok

# --- Гл.1 Закон Ома: U = I R ---
check('Ома U: I=2A R=110Ω -> 220V', 2*110, 220.0)
check('Ома I: U=12V R=4Ω -> 3A', 12/4, 3.0)
check('Ома R: U=230V I=10A -> 23Ω', 230/10, 23.0)
# последовательно/параллельно
check('R посл. 100+200 -> 300', 100+200, 300.0)
check('R пар. 100||100 -> 50', 1/(1/100+1/100), 50.0)

# --- Гл.2 Мощность P=UI и энергия E=P t ---
check('P чайник 230V*8.7A -> ~2001W', 230*8.7, 2001.0)
check('P=U^2/R 230^2/52.9 -> ~1000W', 230**2/52.9, 1000.0, )  # tol relative
check('Энергия 2кВт*1.5ч -> 3 кВт·ч', 2*1.5, 3.0)
check('Лампа 10Вт*5ч -> 0.05 кВт·ч', 10/1000*5, 0.05)

# --- Гл.3 Счёт: кВт·ч * тариф ---
TARIFF = 6.0  # руб/кВт·ч (пример)
check('Счёт 300 кВт·ч * 6 -> 1800 руб', 300*TARIFF, 1800.0)
# чайник 2 кВт, 0.1 ч/день, 30 дней
kwh_month = 2*0.1*30
check('Чайник в месяц кВт·ч', kwh_month, 6.0)
check('Чайник в месяц руб', kwh_month*TARIFF, 36.0)
# двухзонный: день 7, ночь 2
check('Двухзонный: 100*7+50*2 -> 800', 100*7+50*2, 800.0)

# --- Гл.4 Безопасность: ток через тело I=U/R; нагрузка удлинителя ---
check('Тело сухое 230V/100kΩ -> 2.3мА', 230/100000*1000, 2.3)
check('Тело влажное 230V/1kΩ -> 230мА (опасно)', 230/1000*1000, 230.0)
# удлинитель 10А*230В = предел мощности
check('Предел удлинителя 10A*230V -> 2300W', 10*230, 2300.0)
# чайник 2000 + обогреватель 1500 = 3500 > 2300 -> перегруз
total = 2000+1500
check('Чайник+обогреватель сумма Вт', total, 3500.0)
assert total > 10*230, 'должна быть перегрузка'

# --- Гл.5 Устройства/батареи: Вт·ч = А·ч * В; время работы; КПД ---
check('Пауэрбанк 10000мА·ч*3.7В -> 37 Вт·ч', 10000/1000*3.7, 37.0)
check('Время: 37Вт·ч / 5Вт -> 7.4 ч', 37/5, 7.4)
# КПД зарядки: отдано 37, из розетки 46.25 -> 80%
check('КПД 37/46.25 -> 0.8', 37/46.25, 0.8)
# проценты заряда
check('Заряд 80% от 4000мА·ч -> 3200', 0.8*4000, 3200.0)

print('=== ПРОВЕРКА ФОРМУЛ КУРСА ===')
bad = 0
for name, got, exp, ok in checks:
    flag = 'OK ' if ok else 'FAIL'
    if not ok: bad += 1
    print(f'  [{flag}] {name:42s} got={got:.4g} exp={exp:.4g}')
print(f'--- {len(checks)-bad}/{len(checks)} прошли ---')
raise SystemExit(1 if bad else 0)
