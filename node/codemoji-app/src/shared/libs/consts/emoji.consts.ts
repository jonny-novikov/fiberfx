/**
 * Плоский массив всех эмодзи из всех категорий
 * Используется для бегущей строки и других компонентов
 */
export const ALL_EMOJIS: string[] = Object.values(EMOJI_CATEGORIES).flatMap(
  (category) => category.emojis
);

/**
 * Получить все эмодзи из конкретных категорий
 */
export const getEmojisByCategories = (
  categories: EmojiCategoryKey[]
): string[] => {
  return categories.flatMap((key) => EMOJI_CATEGORIES[key].emojis);
};

/**
 * Получить случайный эмодзи
 */
export const getRandomEmoji = (): string => {
  return ALL_EMOJIS[Math.floor(Math.random() * ALL_EMOJIS.length)];
};

/**
 * Получить несколько случайных уникальных эмодзи
 */
export const getRandomEmojis = (count: number): string[] => {
  const shuffled = [...ALL_EMOJIS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, ALL_EMOJIS.length));
};
