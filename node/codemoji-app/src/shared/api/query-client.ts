import { QueryClient } from '@tanstack/react-query'

export const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      // Время "свежести" данных - 30 секунд
      // Пока данные свежие, повторные запросы не выполняются
      staleTime: 30 * 1000,

      // Время хранения данных в кэше - 5 минут
      // После этого неиспользуемые данные удаляются из памяти
      gcTime: 5 * 60 * 1000,

      // Количество автоматических повторных попыток при ошибке - 3 раза
      retry: 3,

      // Задержка между попытками с экспоненциальным ростом
      // 1-я: 1с, 2-я: 2с, 3-я: 4с, максимум 30с
      retryDelay: (attemptIndex) => Math.min(1000 * 2 ** attemptIndex, 30000),

      // Перезагружать данные при возврате на вкладку браузера
      // Полезно для актуализации данных в реальном времени
      refetchOnWindowFocus: true,

      // Перезагружать данные при монтировании компонента
      // Если данные устаревшие, они будут обновлены
      refetchOnMount: true,

      // Перезагружать данные при восстановлении интернет-соединения
      // Полезно для мобильных устройств и нестабильного соединения
      refetchOnReconnect: true,
    },
    mutations: {
      // Мутации повторяются только 1 раз при ошибке
      // Меньше чем у запросов, так как мутации могут изменять данные
      retry: 1,

      // Глобальный обработчик ошибок для всех мутаций
      // Выводит ошибки в консоль в режиме разработки
      onError: (error) => {
        if (import.meta.env.DEV) {
          console.error('[Mutation Error]:', error)
        }
      },
    },
  },
})
